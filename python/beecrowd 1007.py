"""
Leia quatro valores inteiros A, B, C e D. 
A seguir, calcule e mostre a diferença do produto de A e B pelo produto de C e D segundo a fórmula: 
DIFERENCA = (A * B - C * D)
"""

vA = int(input())
vB = int(input())
vC = int(input())
vD = int(input())

DIFERENCA = (vA * vB - vC * vD)

print("DIFERENCA =", DIFERENCA)
